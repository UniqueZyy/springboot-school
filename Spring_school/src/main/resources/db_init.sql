-- 创建捐赠物资类型表
CREATE TABLE IF NOT EXISTS donation_type (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    type_name VARCHAR(100) NOT NULL COMMENT '物资类型名称',
    description VARCHAR(500) COMMENT '物资类型描述',
    status INT DEFAULT 1 COMMENT '状态：0-禁用，1-启用',
    create_time DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    update_time DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间'
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='捐赠物资类型表';

-- 创建捐赠申请表
CREATE TABLE IF NOT EXISTS donation_application (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    applicant_id BIGINT NOT NULL COMMENT '申请人ID',
    applicant_role INT DEFAULT 1 COMMENT '申请人角色：0-管理员，1-学生，2-教师，3-校外人员',
    application_type INT DEFAULT 0 COMMENT '申请类型：0-物资申请，1-资金申请',
    type_id BIGINT NOT NULL COMMENT '物资类型ID',
    quantity INT NOT NULL COMMENT '申请数量/金额',
    reason VARCHAR(500) COMMENT '申请理由',
    status INT DEFAULT 0 COMMENT '状态：0-待审批，1-已批准，2-已拒绝',
    apply_time DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '申请时间',
    approve_time DATETIME COMMENT '审批时间',
    approver_id BIGINT COMMENT '审批人ID',
    approval_comment VARCHAR(500) COMMENT '审批意见',
    FOREIGN KEY (applicant_id) REFERENCES user(id),
    FOREIGN KEY (type_id) REFERENCES donation_type(id),
    FOREIGN KEY (approver_id) REFERENCES user(id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='捐赠申请表';

-- 创建捐赠分配表
CREATE TABLE IF NOT EXISTS donation_distribution (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    type_id BIGINT NOT NULL COMMENT '物资类型ID',
    quantity INT NOT NULL COMMENT '分配数量',
    distribution_object VARCHAR(200) NOT NULL COMMENT '分配对象',
    reason VARCHAR(500) COMMENT '分配原因',
    distribution_time DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '分配时间',
    operator_id BIGINT NOT NULL COMMENT '操作人ID',
    FOREIGN KEY (type_id) REFERENCES donation_type(id),
    FOREIGN KEY (operator_id) REFERENCES user(id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='捐赠分配表';

-- 创建反馈表
CREATE TABLE IF NOT EXISTS feedback (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    type INT NOT NULL COMMENT '反馈类型：1-建议，2-问题，3-投诉，4-其他',
    content TEXT NOT NULL COMMENT '反馈内容',
    submitter_id BIGINT NOT NULL COMMENT '提交人ID',
    status INT DEFAULT 0 COMMENT '状态：0-未处理，1-已处理',
    submit_time DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '提交时间',
    reply_content TEXT COMMENT '回复内容',
    reply_id BIGINT COMMENT '回复人ID',
    reply_time DATETIME COMMENT '回复时间',
    FOREIGN KEY (submitter_id) REFERENCES user(id),
    FOREIGN KEY (reply_id) REFERENCES user(id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='反馈表';

-- 创建公告表
CREATE TABLE IF NOT EXISTS announcement (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    title VARCHAR(200) NOT NULL COMMENT '公告标题',
    content TEXT NOT NULL COMMENT '公告内容',
    publish_time DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '发布时间',
    expire_time DATETIME COMMENT '过期时间',
    is_top INT DEFAULT 0 COMMENT '是否置顶：0-否，1-是',
    status INT DEFAULT 1 COMMENT '状态：0-禁用，1-启用',
    publisher_id BIGINT NOT NULL COMMENT '发布人ID',
    FOREIGN KEY (publisher_id) REFERENCES user(id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='公告表';

