-- 创建数据库
CREATE DATABASE IF NOT EXISTS spring_school CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci;

USE spring_school;

-- 创建角色表
CREATE TABLE IF NOT EXISTS role (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    role_name VARCHAR(50) NOT NULL COMMENT '角色名称',
    role_type INT NOT NULL COMMENT '角色类型：1-学生，2-教师，3-校友，4-校外爱心人士，5-管理员',
    description VARCHAR(200) COMMENT '角色描述',
    permissions TEXT COMMENT '权限标识（JSON格式）',
    status INT DEFAULT 1 COMMENT '状态：0-禁用，1-启用',
    create_time DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    update_time DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
    UNIQUE KEY uk_role_type (role_type)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='角色表';

-- 创建用户表
CREATE TABLE IF NOT EXISTS user (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    username VARCHAR(50) NOT NULL COMMENT '用户名（账号）',
    password VARCHAR(100) NOT NULL COMMENT '密码',
    name VARCHAR(50) NOT NULL COMMENT '姓名',
    email VARCHAR(100) COMMENT '邮箱',
    phone VARCHAR(20) COMMENT '手机号',
    role_type INT NOT NULL COMMENT '角色类型：1-学生，2-教师，3-校友，4-校外爱心人士，5-管理员',
    code VARCHAR(50) COMMENT '学号/工号/校友编号',
    gender INT COMMENT '性别：0-女，1-男',
    status INT DEFAULT 1 COMMENT '状态：0-禁用，1-启用',
    create_time DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    update_time DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
    last_login_time DATETIME COMMENT '最后登录时间',
    UNIQUE KEY uk_username (username),
    FOREIGN KEY (role_type) REFERENCES role(role_type)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='用户表';

-- 创建用户角色关联表
CREATE TABLE IF NOT EXISTS user_role (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    user_id BIGINT NOT NULL COMMENT '用户 ID',
    role_type INT NOT NULL COMMENT '角色类型',
    create_time DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    UNIQUE KEY uk_user_role (user_id, role_type),
    FOREIGN KEY (user_id) REFERENCES user(id) ON DELETE CASCADE,
    FOREIGN KEY (role_type) REFERENCES role(role_type) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='用户角色关联表';

-- 创建捐赠项目表
CREATE TABLE IF NOT EXISTS donation_project (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    project_name VARCHAR(100) NOT NULL COMMENT '项目名称',
    description TEXT COMMENT '项目描述',
    target_amount DECIMAL(10, 2) NOT NULL COMMENT '目标金额',
    current_amount DECIMAL(10, 2) DEFAULT 0 COMMENT '当前金额',
    start_time DATETIME NOT NULL COMMENT '开始时间',
    end_time DATETIME NOT NULL COMMENT '结束时间',
    status INT DEFAULT 1 COMMENT '状态：0-未开始，1-进行中，2-已结束，3-已取消',
    creator_id BIGINT NOT NULL COMMENT '创建者ID',
    create_time DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    update_time DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
    FOREIGN KEY (creator_id) REFERENCES user(id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='捐赠项目表';

-- 创建捐赠记录表
CREATE TABLE IF NOT EXISTS donation_record (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    user_id BIGINT NOT NULL COMMENT '捐赠人ID',
    project_id BIGINT NOT NULL COMMENT '项目ID',
    amount DECIMAL(10, 2) NOT NULL COMMENT '捐赠金额',
    donation_time DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '捐赠时间',
    remark VARCHAR(200) COMMENT '备注',
    anonymous INT DEFAULT 0 COMMENT '是否匿名：0-否，1-是',
    FOREIGN KEY (user_id) REFERENCES user(id),
    FOREIGN KEY (project_id) REFERENCES donation_project(id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='捐赠记录表';

-- 创建物品捐赠记录表（用户捐赠物品）
CREATE TABLE IF NOT EXISTS item_donation (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    donor_id BIGINT NOT NULL COMMENT '捐赠人ID',
    donor_name VARCHAR(50) COMMENT '捐赠人姓名',
    item_type VARCHAR(50) NOT NULL COMMENT '物品类型：书籍、衣物、学习用品、生活用品、其他',
    item_name VARCHAR(100) COMMENT '物品名称',
    quantity INT NOT NULL COMMENT '捐赠数量',
    donation_date DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '捐赠日期',
    remark VARCHAR(500) COMMENT '备注',
    status INT DEFAULT 0 COMMENT '状态：0-待审批，1-已通过，2-已拒绝，3-已寄出',
    FOREIGN KEY (donor_id) REFERENCES user(id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='物品捐赠记录表';

-- 创建资金捐赠记录表（用户捐赠资金）
CREATE TABLE IF NOT EXISTS fund_donation (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    donor_id BIGINT NOT NULL COMMENT '捐赠人ID',
    donor_name VARCHAR(50) COMMENT '捐赠人姓名',
    amount DECIMAL(10, 2) NOT NULL COMMENT '捐赠金额',
    donation_date DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '捐赠日期',
    remark VARCHAR(500) COMMENT '备注',
    status INT DEFAULT 0 COMMENT '状态：0-待支付，1-已支付，2-支付失败',
    FOREIGN KEY (donor_id) REFERENCES user(id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='资金捐赠记录表';

